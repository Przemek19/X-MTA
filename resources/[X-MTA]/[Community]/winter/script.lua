function startWinter()
		n = 0
		part = 1
		zz = 0
		for k,i in ipairs(tab) do
			zz = zz+1
			if zz >= 3 then
				zz = 0
				part = part+1
			end
			setTimer(function(i)			
				n = n+1
				shader_x[i[2]] = dxCreateShader ( "shader/texreplace.fx" )
				if fileExists("images/"..i[2]..".jpg") then
					local tex = dxCreateTexture("images/"..i[2]..".jpg")
					dxSetShaderValue(shader_x[i[2]],"gTexture",tex)
					engineApplyShaderToWorldTexture(shader_x[i[2]],i[1])	
				else
					local tex = dxCreateTexture("images/"..i[2]..".png")
					dxSetShaderValue(shader_x[i[2]],"gTexture",tex)
					engineApplyShaderToWorldTexture(shader_x[i[2]],i[1])	
				end				
			end,50*part,1,i)
		end
end

